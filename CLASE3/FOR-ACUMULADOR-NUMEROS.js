/* La ACUMULACION de numeros en este caso en particular funciona como la sumatoria. */
let sumatoria = 0 
//El 0 el ELEMENTO NEUTRO para la SUMA y RESTA.
//El 1 el ELEMENTO NEUTRO para la MULTIPLICACION y DIVISION
for(let i = 1; i < 6; i++){
    sumatoria = sumatoria + i
    console.log(`i:${i}, acumulador : ${sumatoria}`)
}

