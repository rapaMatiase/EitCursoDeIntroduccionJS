/* Dado un array de nuemros hacer la sumatoria de todas los valores en este. 
NOTA: En acumular el valor de i, acumulan el valor de array en la posicion i
4min
*/

let array = [44,32,656,84,24,-5,3,1-2,23,12*3]

let acumualdor = 0
for(let i = 0 ; i < array.length ; i++){
    acumualdor = acumualdor + array[i]
}
